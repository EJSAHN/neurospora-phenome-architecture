def write_workbook(path: Path, sheets: Dict[str, pd.DataFrame]) -> None:
    ensure_dir(path.parent)
    with pd.ExcelWriter(path, engine="openpyxl") as writer:
        for name, df in sheets.items():
            frame = df if df is not None else pd.DataFrame()
            frame.to_excel(writer, sheet_name=name[:31], index=False)
            ws = writer.book[name[:31]]
            ws.freeze_panes = "A2"
            for i, col in enumerate(frame.columns, start=1):
                values = frame[col].head(250).fillna("").astype(str).tolist() if len(frame) else []
                width = min(max([len(str(col))] + [len(v) for v in values]) + 2, 45)
                ws.column_dimensions[ws.cell(1, i).column_letter].width = max(width, 10)
            for cell in ws[1]:
                cell.style = "Headline 4"


def main() -> None:
    args = parse_args()
    root = Path(args.project_root).expanduser().resolve()
    processed = root / "data" / "processed"
    tables = ensure_dir(root / "reports" / "tables")
    source = ensure_dir(root / "reports" / "source_data" / "sensitivity")

    raw = read_csv_required(find_file(root, [processed / "phenotype_matrix_raw.csv", root / "phenotype_matrix_raw.csv"], ["**/phenotype_matrix_raw.csv"]), "phenotype_matrix_raw.csv")
    baseline = read_csv_required(find_file(root, [processed / "phenotype_matrix_discrete.csv", root / "phenotype_matrix_discrete.csv"], ["**/phenotype_matrix_discrete.csv"]), "phenotype_matrix_discrete.csv")
    annot = read_csv_required(find_file(root, [processed / "gene_annotations.csv", root / "gene_annotations.csv"], ["**/gene_annotations.csv"]), "gene_annotations.csv")
    catalog_path = find_file(root, [processed / "assay_catalog.csv", root / "assay_catalog.csv"], ["**/assay_catalog.csv"])
    catalog = pd.read_csv(catalog_path) if catalog_path else None
    assays = infer_assays(raw, baseline, catalog)
    continuous_assays = infer_continuous_assays(raw, assays, catalog)
    if not continuous_assays:
        raise ValueError("No continuous assays were detected; discretization sensitivity cannot be evaluated.")

    sensitivity = run_discretization_sensitivity(raw, baseline, assays, continuous_assays)
    abnormal = abnormality_table(baseline, assays)
    metabolic, metabolic_coverage = build_metabolic_sources(root, annot)
    modes, rulebook = build_gene_class_modes(annot, metabolic)
    counts = gene_class_counts(modes, args.min_class_size)
    overlaps = gene_class_overlap(modes)
    definition_breakdown = gene_definition_breakdown(annot, modes)
    enrich_modes = enrichment_by_mode(modes, abnormal, args.min_class_size)
    stability = mode_stability(enrich_modes)
    met_axis = metabolic_axis_enrichment(metabolic, abnormal, args.min_class_size)
    met_bias = metabolic_bias_scores(met_axis)
    rand_summary, rand_iters = random_set_bias_null(metabolic, abnormal, met_axis, args.random_set_iterations, np.random.default_rng(args.random_seed + 5), args.min_class_size)
    rxn_corr = reaction_count_correlations(metabolic, abnormal)
    metabolic_summary = build_metabolic_bias_summary(met_axis, met_bias, rand_summary, metabolic_coverage)

    summary = pd.DataFrame([{
        "n_mutants": len(baseline),
        "n_assays": len(assays),
        "continuous_assays_tested": ";".join(continuous_assays),
        "n_discretization_schemes": int(len(sensitivity["disc_schemes"])),
        "n_gene_class_modes": int(modes["mode"].nunique()),
        "random_set_iterations": args.random_set_iterations,
        "min_class_size": args.min_class_size,
        "random_seed": args.random_seed,
    }])

    sheets: Dict[str, pd.DataFrame] = {
        "summary_sensitivity": summary,
        **sensitivity,
        "gene_class_rulebook": rulebook,
        "gene_class_counts": counts,
        "gene_class_overlap": overlaps,
        "gene_class_definition_breakdown": definition_breakdown,
        "gene_class_axis_enrich_modes": enrich_modes.sort_values("fdr_q") if not enrich_modes.empty else enrich_modes,
        "gene_class_mode_stability": stability,
        "metabolic_definition_table": metabolic,
        "metabolic_definition_coverage": metabolic_coverage,
        "metabolic_axis_enrichment": met_axis.sort_values("fdr_q") if not met_axis.empty else met_axis,
        "metabolic_bias_scores": met_bias,
        "metabolic_random_set_summary": rand_summary,
        "metabolic_random_set_iters": rand_iters,
        "metabolic_reaction_count_corr": rxn_corr,
        "metabolic_bias_summary": metabolic_summary,
        "abnormality_table": abnormal,
    }
    out = Path(args.output_workbook) if args.output_workbook else tables / "phenome_sensitivity_results.xlsx"
    write_workbook(out, sheets)

    for name, df in sheets.items():
        if name in {"abnormality_table", "metabolic_random_set_iters"}:
            continue
        df.to_csv(source / f"{name}.csv", index=False)

    print(f"Sensitivity analysis complete: {out}")
    print(summary.to_string(index=False))
    key = sensitivity["disc_exact_minimal"]
    key = key[(key["objective"].isin(["profile_fraction", "cluster_nmi"])) & (key["target_fraction_of_full"].isin([0.90, 0.95, 1.00]))]
    if not key.empty:
        print("\nDiscretization sensitivity key minimal sizes:")
        print(key[["scheme_id", "objective", "target_fraction_of_full", "minimal_n_assays", "example_assay_set"]].to_string(index=False))
    print("\nMetabolic bias summary:")
    print(metabolic_summary[["interpretation", "support_status", "observed_result"]].to_string(index=False))


if __name__ == "__main__":
    with warnings.catch_warnings():
        warnings.simplefilter("ignore", category=FutureWarning)
        main()
