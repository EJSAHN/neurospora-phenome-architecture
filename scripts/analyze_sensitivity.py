"""Sensitivity and robustness checks for the Neurospora phenome-architecture analysis.

This script adds three robustness checks:

1. Discretization sensitivity for continuous phenotype assays.
2. Gene-class annotation quality control and mode-stability analysis.
3. Metabolic-model contrast analysis.

All paths are resolved from --project-root. The script expects the base project
outputs generated by scripts/prepare_phenotype_matrix.py and can optionally
reuse the architecture workbook generated by scripts/analyze_functional_architecture.py.
"""

