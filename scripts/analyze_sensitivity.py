def gene_class_overlap(modes: pd.DataFrame) -> pd.DataFrame:
    class_cols = [c for c in modes.columns if c not in {"ncu_id", "gene_name", "mode"}]
    rows = []
    for mode, group in modes.groupby("mode"):
        for a, b in itertools.combinations(class_cols, 2):
            x = group[a].fillna(False).astype(bool).to_numpy()
            y = group[b].fillna(False).astype(bool).to_numpy()
            both = int((x & y).sum())
            union = int((x | y).sum())
            # Phi coefficient for binary overlap.
            table = np.array([[both, int((x & ~y).sum())], [int((~x & y).sum()), int((~x & ~y).sum())]], dtype=float)
            denom = math.sqrt(table.sum(axis=0).prod() * table.sum(axis=1).prod())
            phi = ((table[0, 0] * table[1, 1]) - (table[0, 1] * table[1, 0])) / denom if denom else np.nan
            rows.append({"mode": mode, "class_a": a, "class_b": b, "n_a": int(x.sum()), "n_b": int(y.sum()), "n_both": both, "jaccard": both / union if union else np.nan, "phi": phi})
    return pd.DataFrame(rows)


def gene_definition_breakdown(annot: pd.DataFrame, modes: pd.DataFrame) -> pd.DataFrame:
    strict = modes[modes["mode"] == "strict"].set_index("ncu_id")
    default = modes[modes["mode"] == "default"].set_index("ncu_id")
    broad = modes[modes["mode"] == "broad"].set_index("ncu_id")
    rows = []
    for cls_name in ["TF", "GPCR", "ser_thr_kinase", "phosphatase"]:
        s = strict[cls_name].astype(bool)
        d = default[cls_name].astype(bool)
        b = broad[cls_name].astype(bool)
        rows.append({"gene_class": cls_name, "strict_n": int(s.sum()), "default_n": int(d.sum()), "broad_n": int(b.sum()),
                     "default_added_by_keyword": int((d & ~s).sum()), "broad_added_beyond_default": int((b & ~d).sum()),
                     "strict_fraction_of_default": int(s.sum()) / max(int(d.sum()), 1), "default_fraction_of_broad": int(d.sum()) / max(int(b.sum()), 1)})
    missing_product = int(annot.get("product_description", pd.Series([np.nan] * len(annot))).isna().sum())
    rows.append({"gene_class": "annotation_missingness", "strict_n": missing_product, "default_n": np.nan, "broad_n": np.nan,
                 "default_added_by_keyword": np.nan, "broad_added_beyond_default": np.nan, "strict_fraction_of_default": np.nan,
                 "default_fraction_of_broad": np.nan, "note": "strict_n stores number of genes missing product_description"})
    return pd.DataFrame(rows)


def enrichment_by_mode(modes: pd.DataFrame, abnormal: pd.DataFrame, min_class_size: int) -> pd.DataFrame:
    rows = []
    axes = ["growth", "asexual_development", "sexual_development"]
    class_cols = [c for c in modes.columns if c not in {"ncu_id", "gene_name", "mode"}]
    for mode, group in modes.groupby("mode"):
        data = abnormal.merge(group, on=["ncu_id", "gene_name"], how="inner")
        for gene_class in class_cols:
            mem = data[gene_class].fillna(False).astype(bool)
            if mem.sum() < min_class_size or (~mem).sum() < min_class_size:
                continue
            for axis in axes:
                col = f"{axis}_abnormal_any"
                if col not in data.columns:
                    continue
                ab = data[col].astype(bool)
                x1, x0 = int((mem & ab).sum()), int((mem & ~ab).sum())
                y1, y0 = int((~mem & ab).sum()), int((~mem & ~ab).sum())
                odds, p = safe_fisher(x1, x0, y1, y0)
                rows.append({"mode": mode, "gene_class": gene_class, "axis": axis, "n_class_genes": int(mem.sum()),
                             "class_abnormal": x1, "class_normal": x0, "nonclass_abnormal": y1, "nonclass_normal": y0,
                             "abnormal_rate_class": x1 / max(x1 + x0, 1), "abnormal_rate_nonclass": y1 / max(y1 + y0, 1),
                             "odds_ratio": odds, "log2_odds_ratio_corrected": log2_or_corrected(x1, x0, y1, y0), "fisher_p": p})
    out = pd.DataFrame(rows)
    if not out.empty:
        out["fdr_q"] = bh_fdr(out["fisher_p"])
    return out


def mode_stability(enrich: pd.DataFrame) -> pd.DataFrame:
    if enrich.empty:
        return pd.DataFrame()
    rows = []
    for (gene_class, axis), group in enrich.groupby(["gene_class", "axis"]):
        vals = group.set_index("mode")
        signs = np.sign(vals["log2_odds_ratio_corrected"].fillna(0).to_numpy())
        nonzero = signs[signs != 0]
        sign_consistent = bool(len(nonzero) > 0 and np.all(nonzero == nonzero[0]))
        rows.append({
            "gene_class": gene_class,
            "axis": axis,
            "n_modes_tested": int(vals.shape[0]),
            "sign_consistent_across_modes": sign_consistent,
            "n_modes_fdr_lt_0_05": int((vals["fdr_q"] < 0.05).sum()),
            "min_fdr_q": float(vals["fdr_q"].min()),
            "max_fdr_q": float(vals["fdr_q"].max()),
            "strict_log2_or": float(vals.loc["strict", "log2_odds_ratio_corrected"]) if "strict" in vals.index else np.nan,
            "default_log2_or": float(vals.loc["default", "log2_odds_ratio_corrected"]) if "default" in vals.index else np.nan,
            "broad_log2_or": float(vals.loc["broad", "log2_odds_ratio_corrected"]) if "broad" in vals.index else np.nan,
            "interpretation_flag": "robust" if sign_consistent and int((vals["fdr_q"] < 0.05).sum()) >= 2 else "definition_sensitive",
        })
    return pd.DataFrame(rows).sort_values(["interpretation_flag", "min_fdr_q", "gene_class", "axis"])


