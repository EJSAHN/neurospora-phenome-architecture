def build_metabolic_sources(root: Path, annot: pd.DataFrame) -> Tuple[pd.DataFrame, pd.DataFrame]:
    base = annot[["ncu_id", "gene_name"]].copy()
    base["ncu_id"] = base["ncu_id"].astype(str).str.upper()
    base["in_carrillo_metabolic_sheet"] = canonical_bool(annot["is_metabolic_gene_carrillo_sheet"]) if "is_metabolic_gene_carrillo_sheet" in annot.columns else False
    architecture_metabolic = read_architecture_metabolic_table(root)
    if architecture_metabolic is not None and "ncu_id" in architecture_metabolic.columns:
        architecture_metabolic = architecture_metabolic.copy()
        architecture_metabolic["ncu_id"] = architecture_metabolic["ncu_id"].astype(str).str.upper()
        keep = [c for c in ["ncu_id", "sbml_reaction_count", "sbml_reaction_examples", "in_iJDZ836_sbml", "mentioned_in_dreyfuss_supp_tables", "dreyfuss_supplementary_annotations"] if c in architecture_metabolic.columns]
        base = base.merge(architecture_metabolic[keep].drop_duplicates("ncu_id"), on="ncu_id", how="left")
    else:
        sbml_df, sbml_genes, _ = parse_sbml_model(root)
        if not sbml_df.empty:
            base = base.merge(sbml_df, on="ncu_id", how="left")
        else:
            base["sbml_reaction_count"] = 0
            base["sbml_reaction_examples"] = ""
        base["in_iJDZ836_sbml"] = base["ncu_id"].isin(sbml_genes)
        mentions = parse_dreyfuss_tables(root)
        base = base.merge(mentions, on="ncu_id", how="left")
    for col in ["in_iJDZ836_sbml", "mentioned_in_dreyfuss_supp_tables"]:
        if col not in base.columns:
            base[col] = False
        base[col] = canonical_bool(base[col])
    if "sbml_reaction_count" not in base.columns:
        base["sbml_reaction_count"] = 0
    base["sbml_reaction_count"] = pd.to_numeric(base["sbml_reaction_count"], errors="coerce").fillna(0).astype(int)
    for col in ["sbml_reaction_examples", "dreyfuss_supplementary_annotations"]:
        if col not in base.columns:
            base[col] = ""
        base[col] = base[col].fillna("").astype(str)
    source_cols = ["in_carrillo_metabolic_sheet", "in_iJDZ836_sbml", "mentioned_in_dreyfuss_supp_tables"]
    base["n_metabolic_definitions"] = base[source_cols].astype(bool).sum(axis=1)
    base["metabolic_any"] = base["n_metabolic_definitions"] >= 1
    base["metabolic_high_confidence"] = base["n_metabolic_definitions"] >= 2
    base["metabolic_consensus_3definitions"] = base["n_metabolic_definitions"] >= 3
    base["sbml_only"] = base["in_iJDZ836_sbml"] & ~base["in_carrillo_metabolic_sheet"] & ~base["mentioned_in_dreyfuss_supp_tables"]
    base["carrillo_only"] = base["in_carrillo_metabolic_sheet"] & ~base["in_iJDZ836_sbml"] & ~base["mentioned_in_dreyfuss_supp_tables"]
    base["dreyfuss_table_only"] = base["mentioned_in_dreyfuss_supp_tables"] & ~base["in_iJDZ836_sbml"] & ~base["in_carrillo_metabolic_sheet"]
    coverage = []
    n_total = len(base)
    for col in source_cols + ["metabolic_any", "metabolic_high_confidence", "metabolic_consensus_3definitions", "sbml_only", "carrillo_only", "dreyfuss_table_only"]:
        coverage.append({"metabolic_definition": col, "n_genes_in_phenome": int(base[col].fillna(False).astype(bool).sum()), "fraction_of_phenome": float(base[col].fillna(False).astype(bool).mean()), "n_total_genes": n_total})
    return base, pd.DataFrame(coverage)


