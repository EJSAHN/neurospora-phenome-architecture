def metabolic_bias_scores(axis_enrich: pd.DataFrame) -> pd.DataFrame:
    rows = []
    if axis_enrich.empty:
        return pd.DataFrame()
    for definition, group in axis_enrich.groupby("metabolic_definition"):
        vals = group.set_index("axis")["log2_odds_ratio_corrected"].to_dict()
        growth = vals.get("growth", np.nan)
        dev = np.nanmean([vals.get("asexual_development", np.nan), vals.get("sexual_development", np.nan)])
        rows.append({"metabolic_definition": definition, "growth_log2_or": growth, "mean_development_log2_or": dev,
                     "growth_minus_development_bias": growth - dev if np.isfinite(growth) and np.isfinite(dev) else np.nan,
                     "supports_growth_dominance": bool(np.isfinite(growth) and growth > 0 and growth > dev)})
    return pd.DataFrame(rows)


def random_set_bias_null(metabolic: pd.DataFrame, abnormal: pd.DataFrame, axis_enrich: pd.DataFrame, iterations: int, rng: np.random.Generator, min_class_size: int) -> Tuple[pd.DataFrame, pd.DataFrame]:
    data = abnormal.merge(metabolic, on=["ncu_id", "gene_name"], how="left")
    axes = ["growth", "asexual_development", "sexual_development"]
    axis_arrays = {axis: data[f"{axis}_abnormal_any"].fillna(False).astype(bool).to_numpy() for axis in axes if f"{axis}_abnormal_any" in data.columns}
    n = len(data)
    obs_bias = metabolic_bias_scores(axis_enrich).set_index("metabolic_definition")
    summary_rows, iter_rows = [], []
    for definition in ["in_iJDZ836_sbml", "in_carrillo_metabolic_sheet", "mentioned_in_dreyfuss_supp_tables", "metabolic_any", "metabolic_high_confidence"]:
        if definition not in data.columns or definition not in obs_bias.index:
            continue
        mem = data[definition].fillna(False).astype(bool).to_numpy()
        k = int(mem.sum())
        if k < min_class_size or n - k < min_class_size:
            continue
        null_biases = []
        for it in range(iterations):
            idx = rng.choice(n, size=k, replace=False)
            rand = np.zeros(n, dtype=bool)
            rand[idx] = True
            log2s = {}
            for axis, ab in axis_arrays.items():
                x1, x0 = int((rand & ab).sum()), int((rand & ~ab).sum())
                y1, y0 = int((~rand & ab).sum()), int((~rand & ~ab).sum())
                log2s[axis] = log2_or_corrected(x1, x0, y1, y0)
            dev = np.nanmean([log2s.get("asexual_development", np.nan), log2s.get("sexual_development", np.nan)])
            bias = log2s.get("growth", np.nan) - dev
            null_biases.append(bias)
            iter_rows.append({"metabolic_definition": definition, "iteration": it + 1, "random_growth_minus_development_bias": bias})
        vals = np.asarray(null_biases, dtype=float)
        obs = float(obs_bias.loc[definition, "growth_minus_development_bias"])
        summary_rows.append({"metabolic_definition": definition, "n_metabolic_genes": k, "observed_growth_minus_development_bias": obs,
                             "null_mean_bias": float(np.nanmean(vals)), "null_sd_bias": float(np.nanstd(vals, ddof=1)),
                             "empirical_p_random_ge_observed": (np.sum(vals >= obs) + 1) / (len(vals) + 1),
                             "empirical_p_random_le_observed": (np.sum(vals <= obs) + 1) / (len(vals) + 1),
                             "observed_percentile_in_random_sets": float(np.mean(vals <= obs)), "random_set_iterations": int(len(vals))})
    return pd.DataFrame(summary_rows), pd.DataFrame(iter_rows)


def reaction_count_correlations(metabolic: pd.DataFrame, abnormal: pd.DataFrame) -> pd.DataFrame:
    data = abnormal.merge(metabolic, on=["ncu_id", "gene_name"], how="left")
    model = data[data.get("in_iJDZ836_sbml", pd.Series(False, index=data.index)).fillna(False).astype(bool)].copy()
    if model.empty or "sbml_reaction_count" not in model.columns:
        return pd.DataFrame()
    rows = []
    for col in ["growth_abnormal_count", "asexual_development_abnormal_count", "sexual_development_abnormal_count", "total_abnormal_count"]:
        if col not in model.columns or model["sbml_reaction_count"].nunique() <= 1:
            continue
        rho, p = stats.spearmanr(model["sbml_reaction_count"], model[col])
        rows.append({"x": "sbml_reaction_count", "y": col, "n_model_genes_in_phenome": int(len(model)), "spearman_rho": rho, "spearman_p": p})
    out = pd.DataFrame(rows)
    if not out.empty:
        out["fdr_q"] = bh_fdr(out["spearman_p"])
    return out


